###"for loops"

#"for loops" are used to iterate over a sequence 
#that can be a list, tuple, dictionary, set, or string or other iterable objects.

# For traversing a list :
list1 = [1,2,3]
print(list1[0])
print(list1[1])
print(list1[2])
#Using "for loop"
for el in list1:
    print(el)

# For traversing a tuple :
tuple1 = (4,5,6)
print(tuple1[0])
print(tuple1[1])
print(tuple1[2])
#using "For loop"
for el in tuple1:
    print(el)

